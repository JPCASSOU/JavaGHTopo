    public void initialiserCadreViseesAntennes(TToporobotStructure dt)
    {
        this.myFDocuTopo = dt;
        listerLesViseesAntennes();
    }
    private void listerLesViseesAntennes()
    {
        int nb = this.myFDocuTopo.getNbViseesAntenne();
        if (nb > 0)
        {   
            ((DefaultTableModel) grdViseesAntennes.getModel()).setRowCount(nb);
            for (int i = 0; i < nb; i++)
            {
                TViseeEnAntenne myVisee = this.myFDocuTopo.getViseeEnAntenne(i);
                grdViseesAntennes.setValueAt(myVisee.SerieDepart, i, 0);
                grdViseesAntennes.setValueAt(myVisee.PtDepart, i, 1);
                grdViseesAntennes.setValueAt(myVisee.IDTerrainStation, i, 2);
                grdViseesAntennes.setValueAt(myVisee.Reseau, i, 3);
                grdViseesAntennes.setValueAt(myVisee.Secteur, i, 4);
                grdViseesAntennes.setValueAt(myVisee.Code, i, 5);
                grdViseesAntennes.setValueAt(myVisee.Expe, i, 6);
                grdViseesAntennes.setValueAt(myVisee.Longueur, i, 7);
                grdViseesAntennes.setValueAt(myVisee.Azimut, i, 8);
                grdViseesAntennes.setValueAt(myVisee.Pente, i, 9);
                grdViseesAntennes.setValueAt(myVisee.Commentaires, i, 10);
            }
        }    
    }   
