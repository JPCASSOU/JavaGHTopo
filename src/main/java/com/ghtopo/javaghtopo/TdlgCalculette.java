    private void btnDeclimagWMMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeclimagWMMActionPerformed
        // TODO add your handling code here:
        double lon  = ensureConvertStrToNumber(editLongitude.getText().trim(),  0.00);
        double lat  = ensureConvertStrToNumber(editLatitude.getText().trim(), 43.00);
        double alt  = ensureConvertStrToNumber(editAltitude.getText().trim(), 0.4);
        int YYYY = strToIntDef(editDeclAnnee.getText().trim(), 2010);
        double decl = myDeclimag.getDeclination(lat, lon, YYYY, alt / 1000);
        editdeclinaison.setText(sprintf("%.8f", decl));
    }//GEN-LAST:event_btnDeclimagWMMActionPerformed

    private void btnDeclimagIGRFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeclimagIGRFActionPerformed
        // TODO add your handling code here: 
        if (fDeclimagOK)
        {
            int JJ   = strToIntDef(editDeclJour.getText().trim(), 1);
            int MM = strToIntDef(editDeclMois.getText().trim(), 1);
            int YYYY = strToIntDef(editDeclAnnee.getText().trim(), 2012);

            double lon  = ensureConvertStrToNumber(editLongitude.getText().trim(),  0.00);
            double lat  = ensureConvertStrToNumber(editLatitude.getText().trim(), 43.00);
            double alt  = ensureConvertStrToNumber(editAltitude.getText().trim(), 0.4);
            double decl = -666.00;
            try
            {
                decl = myCalculateurDeclimag.CalcDeclimag(lon, lat, alt, YYYY, MM, JJ);
                editdeclinaison.setText(sprintf("%.8f", decl));                
            }   
            catch (Exception e)
            {
                e.printStackTrace();
            }
        }    
    }//GEN-LAST:event_btnDeclimagIGRFActionPerformed

