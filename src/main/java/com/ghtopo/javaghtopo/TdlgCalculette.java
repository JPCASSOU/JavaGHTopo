    public void initialiserDlg()
    {
        this.setTitle("Calculette - Convertisseur");
        myConvertisseur = new TConvertisseurCoordonnees();
        fConvertisseurOK = myConvertisseur.initialiser();
        myDeclimag = new TSAGeoMag();
        myCalculateurDeclimag = new TCalculateurDeclimag();
        fDeclimagOK = myCalculateurDeclimag.initialiser(getGHTopoDirectory() + File.separator + "IGRF");
        // activer les panneaux Convertisseur et eclinaison
        pnlConversionCoords.setEnabled(fConvertisseurOK);
        pnlDeclinaisonMagnetique.setEnabled(fDeclimagOK);

        try
        {
            // panneau convertisseur de coordonnees
            if (fConvertisseurOK)
            {    
                // remplissage des combos
                remplirCombosListesSystem(cmbSystSrc, myConvertisseur);
                remplirCombosListesSystem(cmbSystTgt, myConvertisseur);
            }
            // panneau déclinaison magnétique
            if (fDeclimagOK)
            {    
                ; // en attente
            }    
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }    
    }    
       
    
